mystring = "   My String Example   " #declare a string
puts mystring #print a string

puts mystring.upcase(); #turns the string into upper case & outputs it
puts mystring.downcase(); #turns the string into lower case & outputs it
puts mystring.strip(); #removes the extra space from both sides and outputs it
puts mystring.include? "Example" #returns true if it includes the exact word
puts mystring[6] #access the 6th element from a string (it starts from 0)
