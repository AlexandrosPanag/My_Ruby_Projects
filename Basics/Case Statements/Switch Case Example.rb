# Define a variable to switch on
fruit = "apple"

# Use a case statement to check the value of the variable
case fruit
when "apple"
  puts "This is an apple"
when "banana"
  puts "This is a banana"
when "orange"
  puts "This is an orange"
else
  puts "I don't know what fruit this is"
end
